# Faça um código que receba o valor da
# base e da altura de um triângulo e
# calcule sua área. usando a fórmula A =
# (base x Altura ) /2

base = int(input("Digite a base do triangulo: "))
altura = int(input("Digite a altura do triangulo: "))

area = (base * altura) /2
print(f"A area do triangulo: {area}")